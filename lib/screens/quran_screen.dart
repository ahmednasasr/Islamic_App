  void filterSurahs(String query) {
    final filtered = surahname.where((surah) => surah.contains(query)).toList();
    setState(() {
      filteredSurahList = filtered;
    });
  }

  void playOrPauseSurah(int index) async {
    if (playingIndex == index) {
      await audioPlayer.pause();
      setState(() {
        playingIndex = null;
      });
    } else {
      String path = 'quran_audio/${(index + 1).toString().padLeft(3, '0')}.mp3';
      try {
        await audioPlayer.setSource(AssetSource(path));
        await audioPlayer.resume();
        setState(() {
          playingIndex = index;
        });
      } catch (e) {
        print("Error playing audio: $e");
      }
    }
  }
